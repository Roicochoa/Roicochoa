public class ForLoop{
    public static void main(String args[]){
        for(int x = 10; x <=20; x += 1){
            System.out.println("value of x: " + x);
            System.out.println("\n");
            if (x>=20){
                System.out.println("x is greater than or equal to 20");
            }
        }
    }
}